package App.Domain;

public class Coach {
    private int ID;
    private String Name;
    private int Age;
    private int Experience;
    private String Team;

    // Getter for ID
    public int getID() {
        return ID;
    }

    // Setter for ID
    public void setID(int ID) {
        this.ID = ID;
    }

    // Getter for Name
    public String getName() {
        return Name;
    }

    // Setter for Name
    public void setName(String Name) {
        this.Name = Name;
    }

    // Getter for Age
    public int getAge() {
        return Age;
    }

    // Setter for Age
    public void setAge(int Age) {
        this.Age = Age;
    }

    // Getter for Experience
    public int getExperience() {
        return Experience;
    }

    // Setter for Experience
    public void setExperience(int Experience) {
        this.Experience = Experience;
    }

    // Getter for Team
    public String getTeam() {
        return Team;
    }

    // Setter for Team
    public void setTeam(String Team) {
        this.Team = Team;
    }
}
